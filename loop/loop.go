func GetPush() {
	count := 0 // количество отжиманий
	i := 1     // счётчик дней

	// добавьте цикл for с подсчётом отжиманий
	for {
		if i > 100 {
			break
		}

		count += i
		i++
	}

	fmt.Println("Лёха сделает", count, "отжиманий за 100 дней")
}

