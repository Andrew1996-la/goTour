func GetBacteriaCount() {
	count := 2 // количество бактерий
	day := 1   // счётчик дней

	for {
		if day > 30 {
			break
		}

		if day >= 11 {
			count = count - count/10
		}

		pairs := count / 2
		count += pairs

		day++
	}
	fmt.Println("Количество бактерий через 30 дней:", count)
}

